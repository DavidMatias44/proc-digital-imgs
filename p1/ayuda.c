#include <stdio.h>
#include <stdlib.h>

#define SIZE 6

int main(void)
{
    int a[SIZE][SIZE] = {
        { 255, 127,   0,   0, 127, 255 },
        { 127, 255, 127, 127, 255, 127 },
        {   0, 127, 255, 255, 127,   0 },
        {   0, 127, 255, 255, 127,   0 },
        { 127, 255, 127, 127, 255, 127 },
        { 255, 127,   0,   0, 127, 255 }
    };

    int b[SIZE][SIZE] = {
        {   0,   0, 127, 127, 255, 255 },
        {   0,   0, 127, 127, 255, 255 },
        {   0,   0, 127, 127, 255, 255 },
        {   0,   0, 127, 127, 255, 255 },
        {   0,   0, 127, 127, 255, 255 },
        {   0,   0, 127, 127, 255, 255 }
    };

    for (size_t i = 0; i < SIZE; ++i) {
        for (size_t j = 0; j < SIZE; ++j) {
            printf("%4d, %4d |", a[i][j] + b[i][j], a[i][j] - b[i][j]);   
        }
        printf("\n");
    }

    return 0;
}